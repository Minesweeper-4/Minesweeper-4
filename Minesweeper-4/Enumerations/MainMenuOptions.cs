namespace Minesweeper.Enumerations
{
    /// <summary>
    /// Main menu options
    /// </summary>
    public enum MainMenuOptions
    {
        /// <summary>
        /// Starts the application
        /// </summary>
        START,

        /// <summary>
        /// Retrieves the Score Board
        /// </summary>
        SCORES,

        /// <summary>
        /// Allows the user to change the mode of the game
        /// </summary>
        MODE,

        /// <summary>
        /// Set the instruction list
        /// </summary>
        INSTRUCTIONS,

        /// <summary>
        /// Exits the application
        /// </summary>
        EXIT
    }
}
